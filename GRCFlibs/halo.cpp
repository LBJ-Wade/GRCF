#include <math.h>

/*********************************************************************/
/*********************************************************************/

double 
  Fh (double x) 
{
double x1 = x + 1; 
  return log(x1) - x / x1;
}
/*********************************************************************/
/*********************************************************************/

double
  Fhdf (double x)
{
   double x1 = 1.0 + x;

   return x / x1 / x1;
}
